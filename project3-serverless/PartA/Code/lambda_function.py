def preproc(lines):
    lines_new = []
    for word in lines.split():
        word = re.sub("[^a-z]", '', word.strip().lower())
        if word!='' and  word not in stop_words:
            lines_new.append(word)
    return ' '.join(lines_new)


def lambda_handler(event, context):
    
    s3 = boto3.client('s3')
    if event:
        file_obj = event["Records"][0]
        filename = str(file_obj['s3']['object']['key'])
        #print("Filename: ", filename)
        fileObj = s3.get_object(Bucket= "csci5253-fall2018-project3-serverless-ravipati-sivakumar", Key=filename)
        file_content = fileObj["Body"].read().decode('utf-8')
        #print(file_content)
        proc_data = preproc(file_content)
        #print(proc_data)
        s3.put_object(Bucket = "csci5253-fall2018-project3-serverless-output", Key=filename, Body=proc_data)
        
    return 'Done!'
    
